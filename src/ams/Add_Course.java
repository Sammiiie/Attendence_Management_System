    private void buDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buDeleteActionPerformed
        // TODO add your handling code here:
        String[] options = {"Yes", "No"};
        int answ = JOptionPane.showOptionDialog(null, "Sure To Delete??", "Delete Confirm", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[1]);

        if (answ == 0) {
            int index = vTable.getSelectedRow();
            String id = vTable.getValueAt(index, 0).toString();

            if (new DBUpdater().delete(id)) {
                JOptionPane.showMessageDialog(null, "Deleted Updated");

                //CLEAR TXT
                CourseIdT.setText("");
                CourseNt.setText("");
                //teamTxt.setText("");

           retrieve();
            } 
            else
            {
                JOptionPane.showMessageDialog(null, "Not Deleted");
            }

        }

    
    }//GEN-LAST:event_buDeleteActionPerformed

