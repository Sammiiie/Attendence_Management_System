    private void populateTable(){
        String sql = "select * from course";
        try{
            pst = con.prepareStatement(sql);
            result = pst.executeQuery();
            vTable.setModel(DbUtils.resultSetToTableModel(result));
            
        }catch(SQLException ex){
        JOptionPane.showMessageDialog(null, ex);
        }
    }
    
    private void addCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCourseActionPerformed
        // TODO add your handling code here:
        try {
            con = Connection1.connectDB();
            String sql = "insert into course (courseid, coursename) values(?,?)";
            pst = con.prepareStatement(sql);
            String id = courseId.getText();
            String name = courseName.getText();
            pst.setString(1,id);
            pst.setString(2, name);
            //System.out.println(""+id + "  " + pass);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Saved in Course");
   
        } catch (SQLException ex ) {
            System.out.println("no");
            JOptionPane.showMessageDialog(null, ex);

        }
        populateTable();
    }//GEN-LAST:event_addCourseActionPerformed

